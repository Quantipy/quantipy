    def test_create_formats_dict(self):
        dict_exp = self.setup_default_properties_format_dict()
        self.XLSX_Formats.create_formats_dict()
        dict_got = self.XLSX_Formats.format_dict
        self.assertDictEqual(dict_got, dict_exp)
                  
    def assertDictEqual(self, d1, d2, msg=None):
        for k, v1 in d1.iteritems():
            self.assertIn(k, d2, msg)
            v2 = d2[k]
            if isinstance(v1, dict):
                self.assertIsInstance(v2, dict)
                self.assertDictEqual(v1, v2, msg)
            else:
                self.assertEqual(v1, v2, msg)
        return True
    
